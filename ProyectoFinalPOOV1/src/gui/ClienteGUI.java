    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
        String nombre = txtNombre.getText().trim();
String telefono = txtTelefono.getText().trim();
String direccion = txtDireccion.getText().trim();

if (nombre.isEmpty() || telefono.isEmpty() || direccion.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Completa todos los campos.");
    return;
}

Cliente cliente = new Cliente();
cliente.setId_cliente(UUID.randomUUID().toString());
cliente.setCliente_nombre(nombre);
cliente.setCliente_telefono(telefono);
cliente.setCliente_direccion(direccion);

boolean exito = manager.getClienteDAO().Create(cliente);

if (exito) {
    JOptionPane.showMessageDialog(this, "Cliente registrado con Ã©xito.");
    new ProductoGUI(manager, cliente).setVisible(true);
    this.dispose();
} else {
    JOptionPane.showMessageDialog(this, "Error al registrar cliente.");
}
    }//GEN-LAST:event_btnSiguienteActionPerformed

